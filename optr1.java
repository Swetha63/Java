class Optr1{
public static void main(String args[]){
int x=5,y=4;
System.out.println(x+y);//9
System.out.println(x-y);//1
System.out.println(x*y);//20
System.out.println(x/y);//1
System.out.println((double)x/y);//1.25
System.out.println(x/(double)y);//1.25

System.out.println((double)(x/y));//1.0
System.out.println(x%y);//1 as reminder
System.out.println(++x);//6
System.out.println(--y);//3
}
}

